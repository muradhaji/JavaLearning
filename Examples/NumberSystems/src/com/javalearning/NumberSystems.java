public class NumberSystems {

	public static void main(String[] args) {
		int dec = 42;
		System.out.println("Decimal: " + dec);
		
		int bin = 0b00101010;
		System.out.println("Binary: " + bin);
		
		int oct = 052;
		System.out.println("Octal: " + oct);
		
		int hex = 0x2A;
		System.out.println("Hexadecimal: " + hex);
	}

}
